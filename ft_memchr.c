#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	char	*s;

	s = (char *)str;
	while (n > 0)
	{
		if (*s == c)
			return (s);
		s++;
		n--;
	}
	return (0);
}
